          <h1>CEO and Regional Director of Baker Tilly International visited Baker Tilly Azerbaijan</h1>
          <p><b>26/11/2015</b></p>
          <div><p><img alt="" height="366" src="http://www.bakertilly.az/images/18112015.jpg" style="float: left; margin: 0px 8px 40px 0px;" width="272" />Geoff Barnes, the CEO and President of Baker Tilly International and Adam Grainger, EMEA Regional Director of Baker Tilly International, visited Baker Tilly Azerbaijan office on November 18-19, 2015.<br />
<br />
Despite the tight itinerary and heavy business agenda, Mr. Barnes and Mr. Grainger spent two days in Baku office.&nbsp; Mr. Barnes met with students at Azerbaijan Diplomatic Academy (ADA) on Wednesday, November 18. He talked about international marketplace, global economy and business environment. The presentation was followed by Q&amp;A session.&nbsp;</p>

<p>Following the presentation, Geoff Barnes and Adam Grainger had a short staff meeting with Baker Tilly Azerbaijan employees. Mr. Barnes informed colleagues about forthcoming plans of Baker Tilly International and the network&#39;s news.&nbsp;</p>

<p>On November 19, Mr. Barnes and Mr. Grainger together with management of Baker Tilly Azerbaijan participated on Breakfast Briefing with U.S. Embassy Officials at Marriott Absheron Hotel.</p>

<p>CEO of Baker Tilly International Geoff Barnes also participated on Roundtable Discussion with Deputy Minister of Economy and Industry of Azerbaijan and on One-to-One Business Meetings arranged by USACC.</p>

<p>Mr. Barnes and Mr. Grainger also attended AmCham Azerbaijan Luncheon on November 19, 2015.</p>

